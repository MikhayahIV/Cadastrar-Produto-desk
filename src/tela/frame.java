    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        // Deletar Produto por Id
        try{
            Produtos p = new Produtos();
            p.setId(Integer.parseInt(idTxt.getText()));
            
            if (p.deletarPorID()){
            JOptionPane.showMessageDialog(null, "ID: "+idTxt.getText() +" Deletado com sucesso.");
            idTxt.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Nenhum produto encontrado com esse ID.");
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null,"Por favor, digite apenas numeros no campo de id");
        } catch (SQLException | ClassNotFoundException ex ){
            JOptionPane.showMessageDialog(null,"ERROR:" +ex.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

